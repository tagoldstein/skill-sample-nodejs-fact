var data = [
    "Terry is 68 years old.",
    "Terry has a younger sister who is ten and a half years younger than him.",
    "Terry has an older brother that is two and a half years older than him.",
    "Terry will be married for 50 years next month.",
    "Terry lived in the Brownsville section of Brooklyn until he was 8 years old.",
    "Terry grew up in Coney Island.",
    "Terry has two sons and six grandchildren.",
    "Terry served in the United States Navy in the 1960s.",
    "Terry has a rare but benign condition called Mirror Dystonia.",
    "Terry has two different color eyes.",
    "Terry is a terrible clarinet player.",
    "Terry has a resting heart beat of only 53 beats per minute.",
    "Terry has lived in New Jersey since 1978."
];

